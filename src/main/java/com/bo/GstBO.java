package com.bo;

public class GstBO {

	private int id;
	private String gst_no;
	private String gst_percentage;
	private String gst_amount;
	

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getGst_no() {
		return gst_no;
	}

	public void setGst_no(String gst_no) {
		this.gst_no = gst_no;
	}

	public String getGst_percentage() {
		return gst_percentage;
	}

	public void setGst_percentage(String gst_percentage) {
		this.gst_percentage = gst_percentage;
	}

	public String getGst_amount() {
		return gst_amount;
	}

	public void setGst_amount(String gst_amount) {
		this.gst_amount = gst_amount;
	}
	
	

}
